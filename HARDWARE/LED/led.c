//初始化PB5和PE5为输出口.并使能这两个口的时钟		    
//LED IO初始化
void LED_Init(void)
{
 
 GPIO_InitTypeDef  GPIO_InitStructure;
 	
 RCC_APB2PeriphClockCmd(RCC_APB2Periph_GPIOC|RCC_APB2Periph_GPIOB, ENABLE);	 //使能PB,PE端口时钟
	
 GPIO_InitStructure.GPIO_Pin = GPIO_Pin_13;				 // 端口配置
 GPIO_InitStructure.GPIO_Mode = GPIO_Mode_Out_PP; 		 //推挽输出
 GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;		 //IO口速度为50MHz
 GPIO_Init(GPIOC, &GPIO_InitStructure);					 //根据设定参数初始化GPIOc13
 GPIO_SetBits(GPIOC,GPIO_Pin_13);						 // 输出高

 GPIO_InitStructure.GPIO_Pin = GPIO_Pin_9;				 // 端口配置
 GPIO_Init(GPIOB, &GPIO_InitStructure);					 //根据设定参数初始化GPIOb9
 GPIO_SetBits(GPIOB,GPIO_Pin_9);						 // 输出高

}


void led_runtime(void)
{
    if(sys_10ms_count%100 == 0) //1s反转1次
        LED_RUN=!LED_RUN;	
}

void led_err(void)
{
    LED_ERR=0;
}


